		<h2>Chanel</h2>
		<h3>Brief History</h3>
		<p>The House of Chanel (Chanel S.A.) originated in 1909 when Gabrielle Chanel opened a millinery shop at 160 Boulevard Malesherbes, the ground floor of the Parisian flat of the socialite and textile businessman Étienne Balsan, of whom she was the mistress. Because the Balsan flat also was a salon for the French hunting and sporting élite, Chanel had the opportunity to meet their demi-mondaine mistresses, who, as such, were women of fashion, upon whom the rich men displayed their wealth — as ornate clothes, jewelry, and hats.</p>
		<p>In the 1920s, the simple-line designs of Chanel couture made popular the "flat-chested" fashions that were the opposite of the hourglass-figure achieved by the fashions of the late 19th century — the Belle Époque of France (ca. 1890–1914), and the British Edwardian era (ca. 1901–1919). Chanel used colors traditionally associated with masculinity in Europe, such as grey and navy blue, to denote feminine boldness of character. The clothes of the House of Chanel featured quilted fabric and leather trimmings; the quilted construction reinforces the fabric, the design, and the finish, producing a garment that maintains its form and function while being worn. An example of such haute couture techniques is the woolen Chanel suit — a knee-length skirt and a cardigan-style jacket, trimmed and decorated with black embroidery and gold-coloured buttons. The complementary accessories were two-tone pump shoes and jewellery, usually a necklace of pearls, and a leather handbag.</p>
