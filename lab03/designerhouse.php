		<h2>Dior</h2>
		<h3>Brief History</h3>
		<p>The House of Dior was established on 16 December 1946 in "a private house"[5] at 30 Avenue Montaigne Paris B. However, the current Dior corporation celebrates "1947" as the opening year.[5] Dior was financially backed by wealthy businessman Marcel Boussac.</p>
		<p>On 12 February 1947, Dior launched his first fashion collection for Spring–Summer 1947. The show of "90 models of his first collection on six mannequins" was presented in the salons of the company's headquarters at 30 Avenue Montaigne.Originally, the two lines were named "Corolle" and "Huit".[5] However, the new collection went down in fashion history as the "New Look" after the editor-in-chief of Harper's Bazaar Carmel Snow exclaimed, "It's such a new look!" The New Look was a revolutionary era for women back in the forties.</p>
		<p>The death of the head designer left the House of Dior in chaos, and general manager Jacques Rouët considered shutting down operation worldwide. This possibility was not received graciously by Dior licensees and the French fashion industry; the Maison Dior was too important to the financial stability of the industry to allow such an action. To bring the label back on its feet, Rouët promoted the 21-year-old Yves Saint-Laurent to Artistic Director the same year.</p>
