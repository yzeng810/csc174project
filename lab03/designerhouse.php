		<h2>Louis Vuitton</h2>
		<h3>Brief History</h3>
		<p>The Louis Vuitton label was founded by Vuitton in 1854 on Rue Neuve des Capucines in Paris, France. Louis Vuitton had observed that the HJ Cave Osilite trunk could be easily stacked. In 1858, Vuitton introduced his flat-topped trunks with trianon canvas, making them lightweight and airtight. Before the introduction of Vuitton's trunks, rounded-top trunks were used, generally to promote water runoff, and thus could not be stacked. It was Vuitton's gray Trianon canvas flat trunk that allowed the ability to stack with ease for voyages. Many other luggage makers imitated LV's style and design.</p>
		<p>During this period, Louis Vuitton began to incorporate leather into most of its products, which ranged from small purses and wallets to larger pieces of luggage. In order to broaden its line, the company revamped its signature Monogram Canvas in 1959 to make it more supple, allowing it to be used for purses, bags, and wallets.</p>
